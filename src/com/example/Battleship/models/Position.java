package com.example.Battleship.models;

public class Position {
    private int x;
    private int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getXForDisplaying() {
        return x + 1;
    }

    public char getYForDisplaying() {
        return (char)(y + 65);
    }
}
